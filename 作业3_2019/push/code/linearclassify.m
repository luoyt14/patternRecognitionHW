%% load data
w1 = [2,3;2,2;2,4;3,3;3,4;2.5,3;1.5,2;3.5,2.5;4,4;0.5,0.5];
w2 = [0,2.5;-2,2;-1,-1;1,-2;3,0;-2,-2;-3,-4;-5,-2;4,-1];

%% 最小欧式距离分类
% 计算分类界面
M1 = mean(w1,1)';
M2 = mean(w2,1)';
syms x1 x2;
syms d1 d2;
d1 = x1*M1(1)+x2*M1(2)-1/2*(M1)'*M1;
d2 = x1*M2(1)+x2*M2(2)-1/2*(M2)'*M2;
p = ([x1;x2]'-1/2*((M1)'+(M2)'))*(M1-M2);
% 绘制分类界面
plot_boundary(w1, w2, 1)

